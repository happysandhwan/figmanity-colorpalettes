function buildCategories(): string[] {
  const set = new Set<string>();

  for (const m of MOOD_STYLE) {
    set.add(m);
  }
  for (const s of SCENE_NOUN) {
    set.add(s);
  }

  for (const m of MOOD_STYLE) {
    for (const s of SCENE_NOUN) {
      set.add(`${m} ${s}`);
    }
  }

  return Array.from(set).sort((a, b) => a.localeCompare(b));
}

export const PALETTE_CATEGORIES: readonly string[] = buildCategories();

